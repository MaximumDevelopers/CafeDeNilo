     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        $transaction = DB::table('transactions')
            ->select('id','date', 'email', 'total_price as total')
            ->get();

        if (Auth::check() && Auth::user()->role == 'barista') {
            return redirect('/barista');
        }
        elseif (Auth::check() && Auth::user()->role == 'owner') {
            return view('users.owner.inventory.sales_by_product')->with('transactions', $transaction);
        }
        elseif (Auth::check() && Auth::user()->role == 'admin') {
            return view('users.admin.reports.sales_by_product')->with('transactions', $transaction);
            
        }
        else {
            return view('users.captain_crew.inventory.sales_by_product')->with('transactions', $transaction);
        }    
    }

